class Solution:
    def fourSum(self, nums, target: int):
        ans = set()
        if len(nums) < 4:
            return ans
        nums.sort()
        
        # f1, l1 = 0, len(nums) - 1
        # while f1 <= l1 - 3:
        for f1, l1 in [[i, j] for i in range(len(nums)) for j in range(len(nums)) if i <= j - 3]:
            f2, l2 = f1 + 1, l1 - 1
            
            while f2 < l2:
                # print(nums[f1] + nums[f2] + nums[l1] + nums[l2] )
                print(f1, f2,l2, l1)
                if nums[f1] + nums[f2] + nums[l1] + nums[l2] == target:
                    ans.add((nums[f1], nums[f2], nums[l2], nums[l1]))
                    f2 += 1
                elif nums[f1] + nums[f2] + nums[l1] + nums[l2] < target:
                    f2 += 1
                else:
                    l2 -= 1
            
                        
        # print(ans)
        return [list(i) for i in ans]

